#include <unistd.h>

void	ft_print_comb(void)
{
	char	n1;
	char	n2;
	char	n3;

	n1 = '0' - 1;
	while (++n1 <= '9')
	{
		n2 = n1;
		while (++n2 <= '9')
		{
			n3 = n2;
			while (++n3 <= '9')
			{
				if (n1 != '0' || n2 != '1' || n3 != '2')
				{
					write (1, ", ", 2);
				}
				write (1, &n1, 1);
				write (1, &n2, 1);
				write (1, &n3, 1);
			}
		}
	}
}
