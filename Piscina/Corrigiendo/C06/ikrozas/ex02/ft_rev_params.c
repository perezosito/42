#include <unistd.h>

int	main(int argc, char **argv)
{
	int	i;
	int	x;

	x = 0;
	i = argc -1;
	while (i > 0)
	{
		while (argv[i][x] != '\0')
			x++;
		write(1, argv[i], x);
		write(1, "\n", 1);
		i--;
	}
	return (0);
}
