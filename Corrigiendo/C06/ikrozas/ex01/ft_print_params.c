#include <unistd.h>

int	main(int argc, char **argv)
{
	int	i;
	int	x;
	
	x = 0;
	i = 1;
	while (i < argc)
	{
		while (argv[i][x] != '\0')
			x++;
		write(1, argv[i], x);
		write(1, "\n", 1);
		i++;
	}
	return (0);
}
