#include <unistd.h>

void	ft_print_alphabet(void)
{
	char	ini;
	char	c;
	int		i;

	ini = 'a';
	i = 0;
	while (i <= 25)
	{
		c = ini + i;
		write(1, &c, 1);
		i++;
	}
}
